const Month=(y)=>{
    if (y=='01'){
        return 'Jan'
    }
    else if (y=='02'){
        return 'Feb'
    }
    else if (y=='03'){
        return 'Mar'
    }
    else if (y=='04'){
        return 'Apr'
    }
    else if (y=='05'){
        return 'May'
    }
    else if (y=='06'){
        return 'Jun'
    }
    else if (y=='07'){
        return 'Jul'
    }
    else if (y=='08'){
        return 'Aug'
    }
    else if (y=='09'){
        return 'Sep'
    }
    else if (y=='10'){
        return 'oct'
    }
    else if (y=='11'){
        return 'Nov'
    }
    else if (y=='12'){
        return 'Dec'
    }

}
export default Month;

